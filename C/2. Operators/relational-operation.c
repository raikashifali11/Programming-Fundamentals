#include<stdio.h>

int main(){

    //Relational operators

    printf("%d\n",3 != 4);
    printf("%d\n",3 >= 4);
    printf("%d\n",4 <= 4);
    printf("%d\n",4 == 4);
    printf("%d\n",4 < 4);
    printf("%d\n",4 > 4);
    
    //Output is 1 for true
    //Output is 0 for false
    return 0;
}